
import math
angulo = float(input('Informe o Angulo para calculo: '))

sen = math.sin(angulo)
cos = math.cos(angulo)
tang = math.tan(angulo)

print('Seno: {:.2f}. Coseno: {:.2f}. Tangente: {:.2f}'.format(sen, cos, tang))

