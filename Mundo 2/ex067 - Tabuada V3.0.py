while True:
    num = int(input('Informe um Número para exibir sua Tabuada: '))
    print('\n')
    if num < 0:
        break
    for index in range(1,11):
        print(f'{num} x {index} = {num*index}')
    #print('\n')
    print('-----' * 25)
